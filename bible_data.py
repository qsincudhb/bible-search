def get_all_verses():
    all_verses = []
    for testament, books in BIBLE_DATA.items():
        for book, chapters in books.items():
            for reference, text in chapters.items():
                all_verses.append({
                    'testament': testament,
                    'book': book,
                    'reference': reference,
                    'text': text
                })
    return all_verses
